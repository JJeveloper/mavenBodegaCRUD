    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        int fila = tblProveedor.getSelectedRow();
        if (fila != -1) {

            if (tblProveedor.getValueAt(fila, 1).toString().equals("cedula")) {
                cmbTipoDocumento.setSelectedIndex(0);
            } else {
                cmbTipoDocumento.setSelectedIndex(1);
            }

            txtDocumento.setText((String) tblProveedor.getValueAt(fila, 2));
            txtNombre.setText((String) tblProveedor.getValueAt(fila, 3));
            txtTelefono.setText((String) tblProveedor.getValueAt(fila, 4));
            txtCorreo.setText((String) tblProveedor.getValueAt(fila, 5));

        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila", "Tabla Proveedor", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSeleccionarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        if (!txtDocumento.getText().trim().isEmpty() && !txtNombre.getText().trim().isEmpty() && !txtTelefono.getText().trim().isEmpty() && !txtCorreo.getText().trim().isEmpty()) {
            pE.setIdtipodocumento(cmbTipoDocumento.getSelectedIndex() + 1);
            System.out.println("cmbTipoDocumento.getSelectedIndex" + cmbTipoDocumento.getSelectedIndex() + 1);
            pE.setDocumento(txtDocumento.getText());
            pE.setNombreproveedor(txtNombre.getText());
            pE.setTelefono(txtTelefono.getText());
            pE.setCorreo(txtCorreo.getText());
            proveedorD.actualizarProveedor(pE);

            buscar();
            limpiar();

        }


    }//GEN-LAST:event_btnEditarActionPerformed

    public void buscar() {

        ProveedorDao pDao = new ProveedorDao();

        DefaultTableModel tblBuscar = pDao.buscarProveedorXDocumentoNombre(txtBuscar.getText());

        tblProveedor.setModel(tblBuscar);

        tblProveedor.getColumnModel().getColumn(0).setPreferredWidth(14);

    }

    public void limpiar() {
        txtDocumento.setText("");
        txtNombre.setText("");
        txtTelefono.setText("");
        txtCorreo.setText("");
    }

