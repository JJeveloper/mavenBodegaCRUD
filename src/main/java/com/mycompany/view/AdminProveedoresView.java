    public void comboTipoDocumento() {

        ArrayList<TipoDocumentoEntity> listaTD = tpDao.mostrarTipoDocumento();

        cmbTipoDocumento.removeAllItems();

        for (int i = 0; i < tpDao.mostrarTipoDocumento().size(); i++) {
            int id = listaTD.get(i).getIdtipoDocumento();
            String tipoD = listaTD.get(i).getTipoDocumento();
            cmbTipoDocumento.addItem(new TipoDocumentoEntity(id, tipoD));
        }

    }


